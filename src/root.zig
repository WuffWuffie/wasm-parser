const std = @import("std");
const Io = std.Io;

pub fn module(reader: *Io.Reader) !void {
    const info = try reader.takeStruct(
        extern struct { magic: u32, version: u32 },
        .little,
    );
    if (info.magic != 0x6D736100) return error.InvalidWasm;
    if (info.version != 1) return error.InvalidWasm;
}

pub fn string(reader: *Io.Reader, allocator: std.mem.Allocator) ![]const u8 {
    const sz = try reader.takeLeb128(u32);
    return reader.readAlloc(allocator, sz);
}

pub fn funcType(reader: *Io.Reader) !void {
    const byte = try reader.takeByte();
    if (byte != 0x60) return error.ParseError;
}

pub const ValType = enum(u8) {
    i32 = 0x7F,
    i64 = 0x7E,
    f32 = 0x7D,
    f64 = 0x7C,
    v128 = 0x7B,
};

pub fn valType(reader: *Io.Reader) !ValType {
    return reader.takeEnum(ValType, .little);
}

pub const RefType = enum(u8) {
    funcref = 0x70,
    externref = 0x6F,
};

pub fn refType(reader: *Io.Reader) !RefType {
    return reader.takeEnum(RefType, .little);
}

pub const Limits = struct {
    flags: Flags,
    min: u32,
    max: u32,

    pub const Flags = packed struct(u8) {
        has_max: bool,
        is_shared: bool,
        reserved: u6 = 0,
    };
};

pub fn limits(reader: *Io.Reader) !Limits {
    const flags = try reader.takeStruct(Limits.Flags, .little);
    const min = try reader.takeLeb128(u32);
    var max: u32 = 0;
    if (flags.has_max) {
        max = try reader.takeLeb128(u32);
    }
    return Limits{
        .flags = flags,
        .min = min,
        .max = max,
    };
}

pub const Section = enum(u8) {
    custom,
    type,
    import,
    function,
    table,
    memory,
    global,
    @"export",
    start,
    element,
    code,
    data,
    data_count,
    tag,
    _,
};

pub fn section(reader: *Io.Reader, buffer: []u8) !?struct { Section, Io.Reader.Limited } {
    const kind = reader.takeEnumNonexhaustive(Section, .little) catch |err| {
        if (err == error.EndOfStream) return null;
        return err;
    };
    const size = try reader.takeLeb128(u32);
    const sub_reader = reader.limited(.limited(size), buffer);
    return .{ kind, sub_reader };
}

pub const ExternKind = enum(u8) {
    func,
    table,
    memory,
    global,
};

pub fn externKind(reader: *Io.Reader) !ExternKind {
    return reader.takeEnum(ExternKind, .little);
}

pub const BlockType = enum(i32) {
    empty = -64,
    i32 = -1,
    i64 = -2,
    f32 = -3,
    f64 = -4,
    v128 = -5,
    _,

    pub fn id(self: BlockType) u32 {
        return @bitCast(@intFromEnum(self));
    }
};

pub fn blockType(reader: *Io.Reader) !BlockType {
    return @enumFromInt(try reader.takeLeb128(i32));
}

pub const Mut = enum(u8) {
    @"const",
    mut,
};

pub fn mut(reader: *Io.Reader) !Mut {
    return reader.takeEnum(Mut, .little);
}

pub const Table = struct {
    ref_type: RefType,
    limits: Limits,
};

pub fn table(reader: *Io.Reader) !Table {
    return .{
        .ref_type = try refType(reader),
        .limits = try limits(reader),
    };
}

pub const Memory = struct {
    limits: Limits,
};

pub fn memory(reader: *Io.Reader) !Memory {
    return .{ .limits = try limits(reader) };
}

pub const GlobalType = struct {
    type: ValType,
    mut: Mut,
};

pub fn globalType(reader: *Io.Reader) !GlobalType {
    return .{
        .type = try valType(reader),
        .mut = try mut(reader),
    };
}

pub const ElemVariant = enum(u8) {
    active_zero,
